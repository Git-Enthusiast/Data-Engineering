# This is a single-line comment
"""
This is a multi-line comment
It can span multiple lines

But technically, it's a multi-line string literal that is not assigned to any variable
and thus ignored by the interpreter.

"""

# You can also use multi-line strings as comments

def add(a, b):
    """
    This function adds two numbers 
    and returns the result.
    """
    return a + b
result = add(5, 3)
print(f"The sum is: {result}")

# Comments are ignored by the Python interpreter
# They are meant for humans to read and understand the code better

